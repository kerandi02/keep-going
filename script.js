/* ── BUILD CAPSULE LIST ── */
function buildCapsules() {
  const grid = document.getElementById('capsules');
  capsules.forEach((c) => {
    const div = document.createElement('div');
    div.className = `capsule ${c.color}`;
    div.innerHTML = `
      <div class="capsule-emoji">${c.emoji}</div>
      <div class="capsule-info">
        <div class="capsule-label">${c.label}</div>
        <div class="capsule-hint">${c.hint}</div>
      </div>
      <div class="capsule-lock">🔒</div>
    `;
    div.addEventListener('click', () => openCapsule(c, div));
    grid.appendChild(div);
  });
}

/* ── OPEN A CAPSULE ── */
function openCapsule(c, el) {
  spawnSparkles(el);
  document.getElementById('m-emoji').textContent = c.emoji;
  document.getElementById('m-title').textContent = c.label;
  document.getElementById('m-sub').textContent   = c.hint;
  document.getElementById('m-msg').textContent   = c.message;
  document.getElementById('modal').classList.add('open');
  document.body.style.overflow = 'hidden';
  spawnConfetti();
}

/* ── CLOSE MODAL ── */
function closeModal(e) {
  if (e.target === document.getElementById('modal')) closeModalBtn();
}
function closeModalBtn() {
  document.getElementById('modal').classList.remove('open');
  document.body.style.overflow = '';
}

/* ── SPARKLE EFFECT ── */
function spawnSparkles(el) {
  const rect   = el.getBoundingClientRect();
  const colors = ['#E05C8A','#FF8C42','#9B6EBF','#3CBFAE','#F0A500'];
  for (let i = 0; i < 8; i++) {
    const s = document.createElement('div');
    s.className = 'sparkle';
    s.style.cssText = `
      left: ${rect.left + Math.random() * rect.width}px;
      top:  ${rect.top  + Math.random() * rect.height}px;
      background: ${colors[Math.floor(Math.random() * colors.length)]};
      --tx: ${(Math.random() - 0.5) * 60}px;
      --ty: ${-20 - Math.random() * 40}px;
    `;
    document.body.appendChild(s);
    setTimeout(() => s.remove(), 1200);
  }
}

/* ── CONFETTI EFFECT ── */
function spawnConfetti() {
  const colors = ['#E05C8A','#FF8C42','#9B6EBF','#3CBFAE','#F0A500','#4CAF7D','#4A90D9'];
  for (let i = 0; i < 30; i++) {
    setTimeout(() => {
      const c = document.createElement('div');
      c.className = 'confetti-piece';
      c.style.cssText = `
        left: ${Math.random() * 100}vw;
        background: ${colors[Math.floor(Math.random() * colors.length)]};
        animation-duration: ${1.5 + Math.random()}s;
        animation-delay: ${Math.random() * 0.5}s;
        border-radius: ${Math.random() > 0.5 ? '50%' : '2px'};
      `;
      document.body.appendChild(c);
      setTimeout(() => c.remove(), 2500);
    }, i * 40);
  }
}

/* ── PHOTO UPLOAD ── */
function handlePhoto(input) {
  const file = input.files[0];
  if (!file) return;
  const reader = new FileReader();
  reader.onload = e => {
    const img = document.createElement('img');
    img.src = e.target.result;
    const display = document.getElementById('photo-display');
    display.innerHTML = '';
    display.appendChild(img);
  };
  reader.readAsDataURL(file);
}

